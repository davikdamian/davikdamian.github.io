-- Avaliações e comentários dos livros/anúncios.
-- Cada usuário pode avaliar o mesmo anúncio uma vez; se avaliar novamente, o sistema atualiza a avaliação.
create table if not exists public.avaliacoes_livros (
  id bigint generated by default as identity primary key,
  livro_id bigint not null references public.livros(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  nota int not null check (nota between 1 and 5),
  comentario text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_avaliacoes_livros_livro_id on public.avaliacoes_livros(livro_id);
create index if not exists idx_avaliacoes_livros_user_id on public.avaliacoes_livros(user_id);

do $$
begin
  if not exists (
    select 1 from pg_constraint where conname = 'avaliacoes_livros_livro_user_unique'
  ) then
    alter table public.avaliacoes_livros
    add constraint avaliacoes_livros_livro_user_unique unique (livro_id, user_id);
  end if;
end $$;

alter table public.avaliacoes_livros enable row level security;

drop policy if exists "avaliacoes_select_auth" on public.avaliacoes_livros;
drop policy if exists "avaliacoes_insert_own" on public.avaliacoes_livros;
drop policy if exists "avaliacoes_update_own" on public.avaliacoes_livros;
drop policy if exists "avaliacoes_delete_own_or_admin" on public.avaliacoes_livros;

create policy "avaliacoes_select_auth" on public.avaliacoes_livros
for select using (auth.uid() is not null);

create policy "avaliacoes_insert_own" on public.avaliacoes_livros
for insert with check (auth.uid() = user_id);

create policy "avaliacoes_update_own" on public.avaliacoes_livros
