-- Pedidos/interesses de compra.
-- Esta tabela deixa a função de compra mais organizada: o pedido fica registrado e também gera conversa no chat.
create table if not exists public.pedidos (
  id bigint generated by default as identity primary key,
  comprador_id uuid not null references public.profiles(id) on delete cascade,
  vendedor_id uuid not null references public.profiles(id) on delete cascade,
  livro_id bigint references public.livros(id) on delete set null,
  status text not null default 'aberto' check (status in ('aberto', 'em_negociacao', 'concluido', 'cancelado')),
  valor numeric(10,2) not null default 0,
  forma_entrega text default 'combinar',
  observacao text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists idx_pedidos_comprador on public.pedidos(comprador_id);
create index if not exists idx_pedidos_vendedor on public.pedidos(vendedor_id);
create index if not exists idx_pedidos_livro on public.pedidos(livro_id);
create index if not exists idx_pedidos_status on public.pedidos(status);

alter table public.pedidos enable row level security;

drop policy if exists "pedidos_select_own_or_admin" on public.pedidos;
drop policy if exists "pedidos_insert_buyer" on public.pedidos;
drop policy if exists "pedidos_update_own_or_admin" on public.pedidos;
drop policy if exists "pedidos_delete_admin" on public.pedidos;

create policy "pedidos_select_own_or_admin" on public.pedidos
for select using (
  auth.uid() = comprador_id
  or auth.uid() = vendedor_id
  or public.is_admin()
);

create policy "pedidos_insert_buyer" on public.pedidos
for insert with check (auth.uid() = comprador_id);

create policy "pedidos_update_own_or_admin" on public.pedidos
for update using (
  auth.uid() = comprador_id
  or auth.uid() = vendedor_id
  or public.is_admin()
) with check (
  auth.uid() = comprador_id
  or auth.uid() = vendedor_id
  or public.is_admin()
);

create policy "pedidos_delete_admin" on public.pedidos
for delete using (
  public.is_admin()
);

