-- Função auxiliar para policies de admin sem causar recursão nas policies da tabela profiles.
create or replace function public.is_admin(check_user uuid default auth.uid())
returns boolean
language sql
security definer
set search_path = public
as $$
  select coalesce((select p.is_admin from public.profiles p where p.id = check_user), false);
$$;

