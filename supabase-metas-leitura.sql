-- Tabelas para o módulo Minha Biblioteca / Metas de Leitura
-- Execute este arquivo no SQL Editor do Supabase.

create table if not exists public.biblioteca_usuario (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  titulo text not null,
  autor text,
  total_paginas int not null check (total_paginas > 0),
  pagina_atual int not null default 0 check (pagina_atual >= 0),
  status text not null default 'quero_ler' check (status in ('quero_ler', 'lendo', 'lido')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  concluido_em timestamptz
);

create table if not exists public.metas_leitura (
  id bigint generated by default as identity primary key,
  user_id uuid not null unique references public.profiles(id) on delete cascade,
  meta_diaria_paginas int not null default 20 check (meta_diaria_paginas > 0),
  meta_mensal_livros int not null default 1 check (meta_mensal_livros > 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.registros_leitura (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  biblioteca_id bigint not null references public.biblioteca_usuario(id) on delete cascade,
  paginas_lidas int not null check (paginas_lidas > 0),
  created_at timestamptz not null default now()
);

create index if not exists idx_biblioteca_usuario_user_id on public.biblioteca_usuario(user_id);
create index if not exists idx_biblioteca_usuario_status on public.biblioteca_usuario(status);
create index if not exists idx_metas_leitura_user_id on public.metas_leitura(user_id);
create index if not exists idx_registros_leitura_user_id on public.registros_leitura(user_id);
create index if not exists idx_registros_leitura_data on public.registros_leitura(created_at);

alter table public.biblioteca_usuario enable row level security;
alter table public.metas_leitura enable row level security;
alter table public.registros_leitura enable row level security;

